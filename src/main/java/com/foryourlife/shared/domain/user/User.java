package com.foryourlife.shared.domain.user;

public class User {
    private String id;
    private String email;
    private String password;
    private String name;
    private String phone;

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }


}
